# Таблица перестановок E-бокса (E-box)
E_BOX = [
    32,  1,  2,  3,  4,  5,
     4,  5,  6,  7,  8,  9,
     8,  9, 10, 11, 12, 13,
    12, 13, 14, 15, 16, 17,
    16, 17, 18, 19, 20, 21,
    20, 21, 22, 23, 24, 25,
    24, 25, 26, 27, 28, 29,
    28, 29, 30, 31, 32,  1
]

# Функция расширения 32-битного числа до 48 бит
def expand(input_32bit):
    output_48bit = 0
    for i in range(48):
        # Извлекаем бит, указанный в таблице E-бокса, и помещаем его в соответствующую позицию
        bit_position = E_BOX[i] - 1  # E-бокс использует 1-индексацию, а Python работает с 0-индексацией
        bit_value = (input_32bit >> bit_position) & 1  # Извлекаем бит с позиции bit_position
        output_48bit = (output_48bit << 1) | bit_value  # Добавляем этот бит в выходное число
    return output_48bit

# Пример использования
input_32bit = 0x12345678  # Пример 32-битного числа
output_48bit = expand(input_32bit)
print(f'Expanded 48-bit output: {bin(output_48bit)[2:].zfill(48)}')